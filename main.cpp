
#include <iostream>
using namespace std;

int main()
{
    int val;

    std::cout << "Enter a number: \a";
    std::cin >> val;
    std::cout << "You entered: " << val << std::endl;

    int num1, num2;

    std::cout << "Enter two numbers: \a";
    std::cin >> num1 >> num2;
    std::cout << "You entered: " << num1 << " and " << num2 << std::endl;
    return 0;
}
